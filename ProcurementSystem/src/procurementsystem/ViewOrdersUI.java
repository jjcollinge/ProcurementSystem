    private void reverseList() {
        ArrayList<Order> reversedList = new ArrayList<>();
        for(int i = ordersModel.size() - 1; i >= 0; i--) {
            reversedList.add(ordersModel.get(i));
        }
        ordersModel.clear();
        reversedList.stream().forEach((item) -> {
            ordersModel.addElement(item);
        });
    }

